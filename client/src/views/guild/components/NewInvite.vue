<template>
  <div>
    <loading-spinner :loading="isLoading"/>

    <h5>Invite Player</h5>

    <form-error-list :errors="errors"/>
      
    <form @submit="handleSubmit">
        <div class="row no-gutters">
            <div class="col">
                <input type="text" required="required" class="form-control" name="username" v-model="username" :disabled="isLoading" placeholder="Enter Player Name...">
            </div>
            <div class="col-auto ml-2">
                <button type="submit" class="btn btn-success" :disabled="isLoading">
                    <i class="fas fa-user-plus"></i>
                    Invite
                </button>
            </div>
        </div>
    </form>
  </div>
</template>

<script>
import FormErrorList from '../../components/FormErrorList'
import LoadingSpinner from '../../components/LoadingSpinner'
import GuildApiService from '../../../services/api/guild'

export default {
  components: {
    'form-error-list': FormErrorList,
    'loading-spinner': LoadingSpinner
  },
  props: {
      guildId: String
  },
  data () {
    return {
      isLoading: false,
      errors: [],
      username: ''
    }
  },
  methods: {
    async handleSubmit (e) {
      this.errors = []

      if (!this.username) {
        this.errors.push('Username is required.')
      }

      e.preventDefault()

      if (this.errors.length) return

      try {
        this.isLoading = true

        let response = await GuildApiService.invite(this.guildId, this.username)

        if (response.status === 200) {
          this.$toasted.show(`You invited ${this.username} to the guild.`, { type: 'success' })

          this.$emit('onUserInvited', response.data)

          this.username = ''
        }
      } catch (err) {
        console.log(err)
        this.errors = err.response.data.errors || []
      }

      this.isLoading = false
    }
  }
}
</script>

