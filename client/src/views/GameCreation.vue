<script>
import LoadingSpinnerVue from '../components/LoadingSpinner'
import ViewContainer from '../components/ViewContainer'
import ViewCollapsePanel from '../components/ViewCollapsePanel'
import ViewTitle from '../components/ViewTitle'
import ViewSubtitle from '../components/ViewSubtitle'
import FormErrorList from '../components/FormErrorList'
import HelpTooltip from '../components/HelpTooltip'
import SpecialistBanListSelection from '../components/game/specialist/SpecialistBanListSelection'
import FluxBar from '../components/game/menu/FluxBar'
import gameService from '../services/api/game'
import router from '../router'

export default {
  components: {
    'loading-spinner': LoadingSpinnerVue,
    'view-container': ViewContainer,
    'view-collapse-panel': ViewCollapsePanel,
    'view-title': ViewTitle,
    'view-subtitle': ViewSubtitle,
    'form-error-list': FormErrorList,
    'help-tooltip': HelpTooltip,
    'specialist-ban-list-selection': SpecialistBanListSelection,
    'flux-bar': FluxBar
  },
  data () {
    return {
      isCreatingGame: false,
      errors: [],
      settings: null,
      options: null
    }
  },
  async mounted () {
    try {
      let response = await gameService.getDefaultGameSettings()

      this.settings = response.data.settings
      this.options = response.data.options
    } catch (err) {
      console.error(err)
    }
  },
  methods: {
    async handleSubmit (e) {
      this.errors = []

      if (!this.settings.general.name) {
        this.errors.push('Game name required.')
      }

      e.preventDefault()

      if (this.errors.length) return

      try {
        this.isCreatingGame = true

        // Call the login API endpoint
        let response = await gameService.createGame(this.settings)

        if (response.status === 201) {
          this.$toasted.show(`The game ${this.settings.general.name} has been created.`, { type: 'success' })

          router.push({ name: 'game-detail', query: { id: response.data } })
        }
      } catch (err) {
        this.errors = err.response.data.errors || []
      }

      this.isCreatingGame = false
    },
    onSpecialistBanSelectionChanged (e) {
      this.settings.specialGalaxy.specialistBans = e
    },
    onPlayerLimitChanged (e) {
      this.settings.diplomacy.maxAlliances = this.settings.general.playerLimit - 1;
    }
  }
}
</script>

